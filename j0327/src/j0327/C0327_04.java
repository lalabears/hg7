package j0327;

public class C0327_04 {

	public static void main(String[] args) {
		// java에서는 ;을 잊지말자 
		// 변수타입 변수명 = 값;
		// 변수를 선언하고 출력해보세요 		
		// 1. false
	    boolean b = false; // 변수 선언
	    boolean b1 ;
	    b1 = false;
	    System.out.println(b);
		// 2. 200  (int, long) 둘다 해보기 :정수 
		int i = 200; 
		long l = 200L; 
		System.out.println(200);
		System.out.println(i);
		System.out.println(l);
	    
		// 3. 3.14 (float, double) 둘다 해보기 :실수
		float f = 3.14f;
		double d = 3.14; 
		System.out.println(3.14);
		System.out.println(f);
		System.out.println(d);
		// 4. T ->한글자, 문자 char
		char c = 'T'; // ''따옴표를사용
		System.out.println("T");
		System.out.println(c);
		
		// 5. java -> 문자열 String 
		String str = "java";
		System.out.println("java");
		System.out.println(str);
		
		

	}

}
