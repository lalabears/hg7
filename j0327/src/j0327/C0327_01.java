package j0327;

public class C0327_01 {

	// main + ctrl + space  주석 
	// 한줄 주석 
	/*
	 * 
	 * 여러줄 주석으로 
	 * 사용할 수 있음 
	 * 
	 */
	// ctrl + / : 주석 처리 
	public static void main(String[] args) {
		
		System.out.print("hi"); // 줄바꿈이 포함되어있지 않음
		System.out.println("Hello"); // 줄바꿈이 포함되어있음
		System.out.println("안녕하세요");
		
		// 즉, println : 출력 후 줄바꿈을 한다. 
		
		System.out.println("10+3");
		// " " 안의 문장이 그대로 출력이 됨. 
		
		// 계산의 결과를 출력해준다. 
		System.out.println(10 + 3); 
		System.out.println(10 - 3);
		System.out.println(10 * 3);
		System.out.println(10 / 3);
		System.out.println(10.0 / 3 );
		
	}
	

}
